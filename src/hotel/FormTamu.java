    private void tampilTabel () {
        DefaultTableModel tb = new DefaultTableModel ();
        tb.addColumn("ID Tamu");
        tb.addColumn("Nama Tamu");
        tb.addColumn("Alamat");
        tb.addColumn("J.Kelamin");
        tb.addColumn("No.Telp");
        tb.addColumn("Email");
        
        try {
            con = koneksi.getCon();
            sql = "SELECT * from tamu";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            tb.setRowCount(0);
            int i = 1;
            while (rs.next()) {
                Object[] data = new Object[6];
                
                data [0] = rs.getString("idTam");
                data [1] = rs.getString("namaTam");
                data [2] = rs.getString("alamat");
                data [3] = rs.getString("jkel");
                data [4] = rs.getString("nope");
                data [5] = rs.getString("email");
                tb.addRow(data);
                i++;
            }
             tabel.setModel (tb);
        } catch (Exception e) {
            System.out.println("terjadi kesalahan dalam pengambilan data"+ e);
        }
    }
