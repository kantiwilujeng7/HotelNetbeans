    @Override
    public void tampilData() {
        con = koneksi.getCon();
        try {
            sql = "SELECT * from tamu where idTam='"+txtIdTamu.getText()+"'";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                txtNamaTam.setText(rs.getString(2));
                txtAlamat.setText(rs.getString(3));
                if (rs.getString("jkel").equals("L")){
                    radioPria.setSelected(true);
                }else {
                    radioWanita.setSelected(true);
                }
                txtNope.setText(rs.getString("nope"));
                txtEmail.setText(rs.getNString("email"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Pesan kesalahan hapus data", JOptionPane.ERROR_MESSAGE);
        }
    }

